import React, { useState } from 'react';
import { Calendar, Eye, EyeOff, Loader2, UserPlus } from 'lucide-react';
import { useNavigate } from 'react-router-dom';
import { useAuthStore } from '../store/authStore';
import toast from 'react-hot-toast';

function Register() {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [confirmPassword, setConfirmPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [showConfirmPassword, setShowConfirmPassword] = useState(false);
  const [isLoading, setIsLoading] = useState(false);
  const navigate = useNavigate();
  const signIn = useAuthStore((state) => state.signIn);

  const validateUser = async (email: string) => {
    try {
      console.log('üîç Iniciando valida√ß√£o do usu√°rio:', email);
      
      const apiUrl = `https://registration.themembers.dev.br/api/users/show-email/${encodeURIComponent(email)}/ac9a9355-2b9e-4ef4-a03d-5e98a2ab6b78/aaaf982d-7b29-4923-a7c1-54ee695fb14e`;
      console.log('üì° URL da API:', apiUrl);
      
      const response = await fetch(apiUrl, {
        method: 'GET',
        headers: {
          'Content-Type': 'application/json',
        },
      });
      
      console.log('üìä Status da resposta:', response.status);
      console.log('üìä Status text:', response.statusText);
      console.log('üìä Headers da resposta:', Object.fromEntries(response.headers.entries()));
      
      if (!response.ok) {
        // Tentar ler o corpo da resposta para mais detalhes
        let errorBody = '';
        try {
          errorBody = await response.text();
          console.error('‚ùå Corpo da resposta de erro:', errorBody);
        } catch (bodyError) {
          console.error('‚ùå Erro ao ler corpo da resposta:', bodyError);
        }
        
        throw new Error(`Usu√°rio n√£o encontrado ou n√£o autorizado (Status: ${response.status} - ${response.statusText})`);
      }
      
      const userData = await response.json();
      console.log('‚úÖ Dados do usu√°rio recebidos:', userData);
      
      return userData;
    } catch (error) {
      console.error('‚ùå Erro completo na valida√ß√£o:', error);
      console.error('‚ùå Tipo do erro:', typeof error);
      console.error('‚ùå Stack trace:', error instanceof Error ? error.stack : 'N/A');
      
      // Verificar se √© um erro de rede/CORS
      if (error instanceof TypeError && error.message.includes('fetch')) {
        console.error('üåê Poss√≠vel erro de rede ou CORS detectado');
        throw new Error('Erro de conectividade. Verifique sua conex√£o com a internet ou tente novamente mais tarde.');
      }
      
      throw new Error('Erro ao validar usu√°rio. Verifique se o email est√° correto.');
    }
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    
    if (password !== confirmPassword) {
      toast.error('As senhas n√£o coincidem');
      return;
    }

    if (password.length < 6) {
      toast.error('A senha deve ter pelo menos 6 caracteres');
      return;
    }

    setIsLoading(true);

    try {
      console.log('üöÄ Iniciando processo de registro para:', email);
      
      // First validate the user through the external API
      const userData = await validateUser(email.trim());
      
      // The API returns an object with a user property
      if (!userData || !userData.user || !userData.user.id) {
        console.error('‚ùå Dados do usu√°rio inv√°lidos:', userData);
        throw new Error('Usu√°rio n√£o encontrado ou dados inv√°lidos');
      }

      const user = userData.user;
      console.log('‚úÖ Usu√°rio validado com sucesso:', user);

      // If validation passes, create account in Supabase
      console.log('üìù Criando conta no Supabase...');
      const { supabase } = await import('../lib/supabase');
      const { data, error } = await supabase.auth.signUp({
        email: email.trim(),
        password: password.trim(),
        options: {
          data: {
            full_name: user.name || user.full_name || email.split('@')[0],
            external_user_id: user.id,
          }
        }
      });

      if (error) {
        console.error('‚ùå Erro do Supabase:', error);
        if (error.message.includes('User already registered')) {
          throw new Error('Este email j√° est√° cadastrado. Tente fazer login.');
        }
        throw error;
      }

      if (data.user) {
        console.log('‚úÖ Conta criada com sucesso no Supabase:', data.user.id);
        toast.success('Conta criada com sucesso! Fazendo login...');
        
        // Automatically sign in the user
        console.log('üîê Fazendo login autom√°tico...');
        await signIn(email.trim(), password.trim());
        navigate('/');
      }
    } catch (error: any) {
      console.error('‚ùå Erro no registro:', error);
      console.error('‚ùå Mensagem do erro:', error.message);
      console.error('‚ùå Stack do erro:', error.stack);
      toast.error(error.message || 'Erro ao criar conta. Tente novamente.');
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-gradient-to-br from-blue-50 via-white to-indigo-50 flex flex-col justify-center py-12 sm:px-6 lg:px-8">
      <div className="sm:mx-auto sm:w-full sm:max-w-md">
        <div className="flex justify-center">
          <div className="bg-blue-600 p-3 rounded-2xl shadow-lg">
            <Calendar className="w-8 h-8 text-white" />
          </div>
        </div>
        <h2 className="mt-6 text-center text-3xl font-bold tracking-tight text-gray-900">
          Criar Nova Conta
        </h2>
        <p className="mt-2 text-center text-sm text-gray-600">
          Ou{' '}
          <button
            onClick={() => navigate('/login')}
            className="font-medium text-blue-600 hover:text-blue-500 transition-colors"
          >
            fa√ßa login se j√° tem uma conta
          </button>
        </p>
      </div>

      <div className="mt-8 sm:mx-auto sm:w-full sm:max-w-md">
        <div className="bg-white py-8 px-4 shadow-xl sm:rounded-2xl sm:px-10 border border-gray-100">
          <form className="space-y-6" onSubmit={handleSubmit}>
            <div>
              <label
                htmlFor="email"
                className="block text-sm font-medium text-gray-700 mb-2"
              >
                Email
              </label>
              <div className="relative">
                <input
                  id="email"
                  name="email"
                  type="email"
                  autoComplete="email"
                  required
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                  className="appearance-none block w-full px-4 py-3 border border-gray-300 rounded-xl shadow-sm placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-transparent transition-all duration-200 sm:text-sm"
                  placeholder="seu@email.com"
                />
              </div>
            </div>

            <div>
              <label
                htmlFor="password"
                className="block text-sm font-medium text-gray-700 mb-2"
              >
                Senha
              </label>
              <div className="relative">
                <input
                  id="password"
                  name="password"
                  type={showPassword ? 'text' : 'password'}
                  autoComplete="new-password"
                  required
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  className="appearance-none block w-full px-4 py-3 pr-12 border border-gray-300 rounded-xl shadow-sm placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-transparent transition-all duration-200 sm:text-sm"
                  placeholder="M√≠nimo 6 caracteres"
                />
                <button
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                  className="absolute inset-y-0 right-0 pr-3 flex items-center"
                >
                  {showPassword ? (
                    <EyeOff className="h-5 w-5 text-gray-400 hover:text-gray-600" />
                  ) : (
                    <Eye className="h-5 w-5 text-gray-400 hover:text-gray-600" />
                  )}
                </button>
              </div>
            </div>

            <div>
              <label
                htmlFor="confirmPassword"
                className="block text-sm font-medium text-gray-700 mb-2"
              >
                Confirmar Senha
              </label>
              <div className="relative">
                <input
                  id="confirmPassword"
                  name="confirmPassword"
                  type={showConfirmPassword ? 'text' : 'password'}
                  autoComplete="new-password"
                  required
                  value={confirmPassword}
                  onChange={(e) => setConfirmPassword(e.target.value)}
                  className="appearance-none block w-full px-4 py-3 pr-12 border border-gray-300 rounded-xl shadow-sm placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-blue-500 focus:border-transparent transition-all duration-200 sm:text-sm"
                  placeholder="Confirme sua senha"
                />
                <button
                  type="button"
                  onClick={() => setShowConfirmPassword(!showConfirmPassword)}
                  className="absolute inset-y-0 right-0 pr-3 flex items-center"
                >
                  {showConfirmPassword ? (
                    <EyeOff className="h-5 w-5 text-gray-400 hover:text-gray-600" />
                  ) : (
                    <Eye className="h-5 w-5 text-gray-400 hover:text-gray-600" />
                  )}
                </button>
              </div>
            </div>

            <div>
              <button
                type="submit"
                disabled={isLoading}
                className="group relative w-full flex justify-center py-3 px-4 border border-transparent text-sm font-medium rounded-xl text-white bg-blue-600 hover:bg-blue-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-blue-500 disabled:opacity-50 disabled:cursor-not-allowed transition-all duration-200 shadow-lg hover:shadow-xl"
              >
                {isLoading ? (
                  <>
                    <Loader2 className="w-5 h-5 mr-2 animate-spin" />
                    Criando conta...
                  </>
                ) : (
                  <>
                    <UserPlus className="w-5 h-5 mr-2" />
                    Criar Conta
                  </>
                )}
              </button>
            </div>
          </form>

          <div className="mt-6">
            <div className="relative">
              <div className="absolute inset-0 flex items-center">
                <div className="w-full border-t border-gray-300" />
              </div>
              <div className="relative flex justify-center text-sm">
                <span className="px-2 bg-white text-gray-500">
                  J√° tem uma conta?
                </span>
              </div>
            </div>

            <div className="mt-6">
              <button
                onClick={() => navigate('/login')}
                className="w-full flex justify-center py-3 px-4 border border-gray-300 rounded-xl text-sm font-medium text-gray-700 bg-white hover:bg-gray-50 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-blue-500 transition-all duration-200"
              >
                Fazer Login
              </button>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}

export default Register;